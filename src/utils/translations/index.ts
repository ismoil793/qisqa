export const getBrowserLocale = () => {
  // @ts-ignore
  let defaultBrowserLocale = navigator.language || navigator.userLanguage || 'en';
  defaultBrowserLocale = defaultBrowserLocale.toLocaleLowerCase();

  if (defaultBrowserLocale.startsWith('uz')) return 'uz';
  if (defaultBrowserLocale.startsWith('ru')) return 'ru';

  return 'en';
};
